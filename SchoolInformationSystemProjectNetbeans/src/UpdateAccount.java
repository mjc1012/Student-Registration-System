    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int res = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this account?", "Delete Account", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if(res == JOptionPane.YES_OPTION){
            if(dbh.deleteAccount(Integer.parseInt(textId.getText()))){
                JOptionPane.showMessageDialog(this, "Account Deleted");
                LoginAccount l = new LoginAccount();
                l.setVisible(true);
                l.pack();
                l.setLocationRelativeTo(null);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Account Not Deleted");
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

