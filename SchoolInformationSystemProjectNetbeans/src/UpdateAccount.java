    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        boolean flag = true;
        if(textUsername.getText().equals("")){
            lbl_u.setVisible(true); flag = false;
        }
        if(String.valueOf(textPassword.getPassword()).equals("")){
            lbl_p.setVisible(true); flag = false;
        }
        if(String.valueOf(textPassword2.getPassword()).equals("")){
            lbl_rp.setVisible(true); flag = false;
        }
        if(flag){
            if(!textUsername.getText().equals(LoginAccount.textUsername.getText()) && dbh.searchUsername(textUsername.getText()))
                JOptionPane.showMessageDialog(this, "Username Already Taken");
            else if(!String.valueOf(textPassword.getPassword()).equals(String.valueOf(textPassword2.getPassword())))
                JOptionPane.showMessageDialog(this, "Password Do Not Match");
            else if(textUsername.getText().equals(LoginAccount.textUsername.getText()) && String.valueOf(textPassword.getPassword()).equals(String.valueOf(LoginAccount.textPassword.getPassword())))
                JOptionPane.showMessageDialog(this, "No Changes");
            else if( dbh.updateAccount(Integer.parseInt(textId.getText()), textUsername.getText(), textPassword.getText())){
                JOptionPane.showMessageDialog(this, "Account Updated");
                LoginAccount.textUsername.setText(UpdateAccount.textUsername.getText());
                LoginAccount.textPassword.setText(UpdateAccount.textPassword.getText());
                MainLinkActionPerformed(evt);
            }
            else
                JOptionPane.showMessageDialog(this, "Account Not Updated");
        }
        else
            JOptionPane.showMessageDialog(this, "Some Important Fields Are Empty");
    }//GEN-LAST:event_updateButtonActionPerformed

