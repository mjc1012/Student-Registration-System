    public DisplayTeacher() {
        initComponents();
        try {
            dbh.connectDB();
            //Display all Records 
            int x = 0;
            while(x < 12){
                tableTeacher.getColumnModel().getColumn(x++).setPreferredWidth(100);
            }
            tableTeacher.getColumnModel().getColumn(4).setPreferredWidth(150);
            tableTeacher.getColumnModel().getColumn(6).setPreferredWidth(200);
            tableTeacher.setShowGrid(true);
            ResultSet rs = dbh.displayTeacher();
            while(tableTeacher.getRowCount() > 0){
                ((DefaultTableModel) tableTeacher.getModel()).removeRow(0);
                
            }
            int columns = rs.getMetaData().getColumnCount();
            while(rs.next()){
                Object[] row = new Object[columns];
                for(int i = 1;i<=columns;i++){
                    row[i-1] = rs.getObject(i);
                }
                ((DefaultTableModel) tableTeacher.getModel()).insertRow(rs.getRow()-1, row);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DisplayStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

